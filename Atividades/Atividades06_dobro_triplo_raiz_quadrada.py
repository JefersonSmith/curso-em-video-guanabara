#Exercício Python 006: Crie um algoritmo que leia um número e mostre o seu dobro, triplo e raiz quadrada.
numero = int(input("Digite um número: "))
dobro = numero * 2
triplo = numero * 3
raiz = numero ** (1/2)

print(f'O número digitado foi {numero}, o dobro é {dobro}, o triplo {triplo} e a raiz quadrada é {raiz}')
