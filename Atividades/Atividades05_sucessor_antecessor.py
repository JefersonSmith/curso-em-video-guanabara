#Exercício Python 005: Faça um programa que leia um número Inteiro e mostre na tela o seu sucessor e seu antecessor.

numero = int(input("Digite um número: "))
anterior = numero - 1
posterior = numero + 1

print(f'O número digitado foi {numero}, o número antecessor é {anterior} e o número posterior é {posterior}')
